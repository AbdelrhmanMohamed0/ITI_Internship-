#include <stdio.h>
int main(void)
{
    int num1, num2, num3;
    printf("Please Enter Number 1 : ");
    scanf("%d", &num1);
    printf("Please Enter Number 2 : ");
    scanf("%d", &num2);
    printf("Please Enter Number 3 : ");
    scanf("%d", &num3);
    printf("Number 3: %d\n", num3);
    printf("Number 2: %d\n", num2);
    printf("Number 1: %d\n", num1);
}
